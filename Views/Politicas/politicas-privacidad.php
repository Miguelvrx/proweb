    <h1>Políticas de Privacidad</h1>

    <h2>Introducción</h2>
    <p>En nuestra biblioteca, estamos comprometidos a proteger la privacidad y seguridad de los datos personales de nuestros usuarios. Esta política de privacidad describe cómo recopilamos, usamos, y protegemos la información personal de nuestros usuarios.</p>

    <h2>Información que Recopilamos</h2>
    <p>Recopilamos diferentes tipos de información, incluyendo:</p>
    <ol>
        <li><strong>Datos Personales de Estudiantes</strong>: nombre, código de estudiante, DNI, carrera, dirección, teléfono, y estado.</li>
        <li><strong>Datos de Usuarios</strong>: nombre, usuario, y contraseña encriptada.</li>
        <li><strong>Datos de Autores</strong>: nombre y foto.</li>
        <li><strong>Datos de Libros</strong>: título, cantidad, autor, editorial, año de edición, materia, número de páginas, descripción, y foto.</li>
        <li><strong>Datos de Editoriales</strong>: nombre y estado.</li>
        <li><strong>Datos de Materias</strong>: nombre y estado.</li>
        <li><strong>Datos de Préstamos</strong>: fecha de préstamo, fecha de devolución, cantidad, observación, y estado.</li>
        <li><strong>Configuraciones de Biblioteca</strong>: nombre, teléfono, dirección, correo, y foto.</li>
        <li><strong>Permisos de Usuarios</strong>: detalles de permisos asignados a usuarios.</li>
    </ol>

    <h2>Uso de la Información</h2>
    <p>Usamos la información recopilada para los siguientes propósitos:</p>
    <ol>
        <li><strong>Gestión de la Biblioteca</strong>: Para administrar los préstamos y devoluciones de libros, y para mantener registros precisos de los estudiantes y usuarios.</li>
        <li><strong>Comunicación</strong>: Para contactar a los usuarios y estudiantes en relación con sus préstamos y para proporcionar información relevante sobre la biblioteca.</li>
        <li><strong>Seguridad</strong>: Para proteger la seguridad y la integridad de nuestros sistemas y datos.</li>
    </ol>

    <h2>Compartir Información</h2>
    <p>No compartimos la información personal de nuestros usuarios con terceros, salvo en los siguientes casos:</p>
    <ol>
        <li>Con el consentimiento del usuario.</li>
        <li>Para cumplir con leyes y regulaciones aplicables.</li>
        <li>Para proteger los derechos, propiedad o seguridad de nuestra biblioteca, nuestros usuarios, o el público en general.</li>
    </ol>

    <h2>Seguridad de la Información</h2>
    <p>Implementamos medidas de seguridad técnicas y organizativas adecuadas para proteger la información personal de nuestros usuarios contra pérdida, uso indebido y acceso no autorizado. Estas medidas incluyen:</p>
    <ol>
        <li><strong>Encriptación de contraseñas</strong>: Las contraseñas de los usuarios se almacenan encriptadas.</li>
        <li><strong>Acceso restringido</strong>: Solo el personal autorizado tiene acceso a la información personal.</li>
        <li><strong>Actualizaciones de seguridad</strong>: Mantenemos nuestros sistemas actualizados con los últimos parches de seguridad.</li>
    </ol>

    <h2>Derechos de los Usuarios</h2>
    <p>Los usuarios tienen los siguientes derechos en relación con sus datos personales:</p>
    <ol>
        <li><strong>Acceso</strong>: Derecho a acceder a sus datos personales que poseemos.</li>
        <li><strong>Rectificación</strong>: Derecho a solicitar la corrección de datos inexactos o incompletos.</li>
        <li><strong>Eliminación</strong>: Derecho a solicitar la eliminación de sus datos personales.</li>
        <li><strong>Restricción del procesamiento</strong>: Derecho a solicitar la limitación del procesamiento de sus datos personales.</li>
        <li><strong>Portabilidad de datos</strong>: Derecho a recibir sus datos personales en un formato estructurado, de uso común y lectura mecánica.</li>
    </ol>
    <p>Para ejercer estos derechos, los usuarios pueden contactar con nosotros a través de los datos de contacto proporcionados en la sección de "Contacto".</p>

    <h2>Cambios a esta Política de Privacidad</h2>
    <p>Podemos actualizar esta política de privacidad de vez en cuando. Notificaremos a los usuarios sobre cualquier cambio significativo mediante la publicación de una nueva política de privacidad en nuestro sitio web y, si es necesario, solicitaremos su consentimiento.</p>

